// 배열의 메소드 (Array's Method)
let members = ['아론렘지', '헤리케인', '무사시소코', '스콜스', '드록바', '포그바',];
console.log(members);

// 배열의 첫 요소를 삭제: shift()
members.shift();
console.log(members);

// 배열의 마지막 요소를 삭제:pop()
members.pop();
console.log(members);

//배열의 첫 요소로 값 추가 : unshift(value);
members.unshift('박지성');
console.log(members)

//배열의 마지막 요소로 값 추가 : push(value);
members.push('즐라탄');
console.log(members);